package dataBean;

public class Page {
	private int comics_id;
	private int page;
	private String url;
	
	
	
	
	
	
	
	
	public int getComics_id() {
		return comics_id;
	}
	public void setComics_id(int comics_id) {
		this.comics_id = comics_id;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
